
public class BubbleSort {

	public static void main(String[] args) {
		System.out.println("Original Sequence: ");
		System.out.println("5 2 6 8 1");
		System.out.println("After each pass: ");
		System.out.println("2 5 6 1 8");
		System.out.println("2 5 1 6 8");
		System.out.println("2 1 5 6 8");
		System.out.println("1 2 5 6 8");
	}

}
